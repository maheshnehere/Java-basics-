package com.chatgpt.second;

public class OddEvenNum {
    public static void main(String[] args) {
        int num = 11;

        if (num % 2 == 0)
        {
            System.out.println(num+" :Number is even");
        }
        else
        {
            System.out.println(num+" Number is odd");
        }
    }
}
